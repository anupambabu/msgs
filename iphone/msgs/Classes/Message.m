#import "Message.h"

#import <Three20Core/TTCorePreprocessorMacros.h>

///////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////
@implementation Message

@synthesize ident;
@synthesize label;
@synthesize text;
@synthesize time;

///////////////////////////////////////////////////////////////////////////////////////////////////
- (void)dealloc {
	TT_RELEASE_SAFELY(ident);
	TT_RELEASE_SAFELY(label);
	TT_RELEASE_SAFELY(text);
	TT_RELEASE_SAFELY(time);
	
	[super dealloc];
}

@end